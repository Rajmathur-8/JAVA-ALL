package com.java.ifStatements.examples;

public class IfStatementRunner {

	public static void main(String[] args) {
		int i = 26;
		if (i == 25) {
			System.out.println("i = 25");
		} else if (i == 24) {
			System.out.println("i = 24");
		} else {
			System.out.println("Neither i = 24 nor i = 25");
		}
	}

}
